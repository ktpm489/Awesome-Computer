package in2018.WrapperClassEx;

public class WrapperClassEx {
    public static void main(String[] args) {
        Integer i = 10;
        Double d = 10.1;

        System.out.println(i);
        System.out.println(d);

        i  = i + 20;
        d = d + 0.4;

        System.out.println(i);
        System.out.println(d);
    }
}
